1. Create GitHub Account
   • Go to github.com → Sign up
   • Use your email or Google account

2. Create New Repository
   • Click "+" → New repository
   • Name: designbrief-pro
   • Make it PUBLIC
   • Add README file

3. Upload Your Code
   • In repository → Add file → Upload files
   • Upload index.html
   • Upload any images/assets folder

4. Enable GitHub Pages
   • Settings → Pages
   • Source: main branch
   • Save

5. Your Website is Live!
   • URL: https://yourusername.github.io/designbrief-pro
   • Wait 1-2 minutes